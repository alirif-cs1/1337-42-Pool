char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'A' || str[i] > 'Z') && (str[i] < 'a' || str[i] > 'z'))
			str[i] = str[i];
		else if ((str[i] + 16 > 'z') || (str[i] + 16 > 'Z' && str[i] <= 'Z'))
			str[i] = str[i] + 16 - 26;
		else
			str[i] = str[i] + 16;
		i++;
	}
	return (str);
}
